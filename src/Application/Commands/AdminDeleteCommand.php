<?php


namespace Olympics\Application\Commands;


class AdminDeleteCommand implements CommandInterface
{
    private $id;

    /**
     * AdminDeleteCommand constructor.
     * @param int $id
     */
    public function __construct(int $id)
    {
        $this->id = $id;
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id): void
    {
        $this->id = $id;
    }
}
